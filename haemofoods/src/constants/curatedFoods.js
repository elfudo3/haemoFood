//   written by Sarah Keogh, Consultant Dietitian, with INDI
// - Alcohol iron data: IHA guide table (pg 10)
//
// RATING LOGIC (based on IHA clinical guidance, NOT raw iron numbers alone):
// - "avoid"     = high heme iron (red meat, organ meat, raw shellfish), iron-fortified foods,
//                 very high total iron (>5mg/100g), or alcohol (liver stress)
// - "moderate"  = moderate heme iron (poultry, cooked fish), moderate non-heme iron,
//                 eggs, legumes, some nuts/seeds
// - "safe"      = low iron, dairy (calcium inhibits absorption), most fruits/veg,
//                 tea/coffee (tannins inhibit absorption)
//
// WHY NOT JUST USE IRON MG/100G?
// Heme iron (from animal sources) is absorbed 2-3x more efficiently than non-heme iron
// (from plants). So beef at 2.6mg/100g is MORE dangerous than spinach at 2.7mg/100g.
// The IHA guide confirms this: "Eating more foods containing non-haem iron instead of
// foods with haem iron may be helpful for people with haemochromatosis."
//
// IRON ABSORPTION ENHANCERS (flagged in reasons):
// - Vitamin C increases iron absorption — flagged when >20mg/100g
// - Alcohol increases iron absorption AND causes liver stress
// - Fructose (fruit juices) can increase absorption
//
// IRON ABSORPTION INHIBITORS (noted in reasons):
// - Calcium (dairy) reduces iron absorption
// - Tannins (tea, coffee) reduce iron absorption
// - Phytates (wholegrains, nuts, seeds, legumes) reduce iron absorption
// - Oxalates (spinach, kale, beetroot) reduce iron absorption
// - Dietary fibre reduces iron absorption
// - Eggs contain phosvitin which binds iron

